public class NestedForLoopEg2 {
    public static void main(String[] args) {

        for (int i = 1; i <=6 ; i++) {     //Outer forloop // Row

            for (int j = 1; j <=5 ; j++) {  //inner forloop // column
                System.out.print(j);   //30
            }
            System.out.println();

        }


    }

}
