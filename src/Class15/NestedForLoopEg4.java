public class NestedForLoopEg4 {
    public static void main(String[] args) {

        for (int i = 1; i <=4 ; i++) {     //Outer forloop // Row

            for (int j = 1; j <=5 ; j++) {  //inner forloop // column
                System.out.print(i);   //30
            }
            System.out.println();

        }


    }

}
