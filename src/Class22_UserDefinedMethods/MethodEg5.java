package Class22_UserDefinedMethods;

public class MethodEg5 {

    public static void main(String[] s) {

       maxNum(10,20);  //20, nothing


    }

    public static void maxNum(int a, int b) {
        if (a>b){
            System.out.println(a);;
        }else {
            System.out.println(b);;
        }

    }


}
