package Class09;

public class VowelChecker {

    //A E I O U

    public static void main(String[] args) {

        //Input
        char c = 'Z';

        //Logic & //Output
        if (c == 'A' || c == 'E' || c == 'I' || c == 'O' || c == 'U') {
            System.out.println("It is a vowel");
        } else {
            System.out.println("It is not a vowel");
        }


    }


}



