package Class05;

public class LadderIfElseEg {

    public static void main(String[] args) {
        int a = 0;

        if (a > 0) {
            System.out.println("The given number is positive");
        } else if (a == 0) {
            System.out.println("It is Zero");
        } else {
            System.out.println("The given number is negative");
        }
    }
}



