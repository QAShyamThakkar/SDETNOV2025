package Class03;

public class SwapVariableEg {

    public static void main(String[] args) {

// Input
        int a =1000;
        int b =2000;
        int y = 0 ;

        //Logic
      y=a;  //y 10
      a=b;  //a 20
      b=y;  //b 10

//Output
        System.out.println(a);
        System.out.println(b);

    }


}
