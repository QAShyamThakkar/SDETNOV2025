public class PrintFrom5to1 {

    public static void main(String[] args) {
        //1. Write a Java Program to print from 5 to 1

        int i =5;  //Initialization

        while (i>=1){  //Condition

            System.out.println(i);
            i--;   //Inc/Dec

        }

        // if - will execute the block of statement, if the condition is true
        // while - will execute the block of statement, till the time condition is true








    }


}
