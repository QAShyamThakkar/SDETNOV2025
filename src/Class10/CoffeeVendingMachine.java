package Class10;

public class CoffeeVendingMachine {

    public static void main(String[] args) {

        int totalCoffee = 10;

        while (totalCoffee >=1) {
            System.out.println(totalCoffee + " coffee I have, would you like to have one");
            System.out.println("I got one, thanks");

            totalCoffee--;

        }

        System.out.println("Its over, you can come tomorrow");

    }


}
